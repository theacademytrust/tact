async function main() {
  const feed = await collectFeed();
  await fs.mkdir(pagesRoot, { recursive: true });
  const existingPages = await fs.readdir(pagesRoot, { withFileTypes: true });

  for (const entry of existingPages) {
    if (!entry.isFile() || !entry.name.endsWith(".html")) continue;
    await fs.unlink(path.join(pagesRoot, entry.name));
  }

  for (const item of feed) {
    const fileName = path.basename(item.pageUrl);
    await fs.writeFile(path.join(pagesRoot, fileName), shellFor(item.slug), "utf8");
  }

  await fs.writeFile(feedPath, buildFeedContent(feed), "utf8");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
