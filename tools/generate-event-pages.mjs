import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const eventsRoot = path.join(repoRoot, "content", "events");
const pagesRoot = path.join(repoRoot, "events");
const feedPath = path.join(eventsRoot, "events-feed.js");
const posterNames = new Set(["poster.jpg", "poster.jpeg", "poster.png", "poster.webp", "poster.svg", "poster.avif"]);
const galleryImageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".avif", ".svg"]);
const feedVersion = "20260501b";
const chromeVersion = "20260501e";

function sanitizeSlug(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function buildPageSlug(date, title, fallbackSlug) {
  const parts = String(date || "").split("-");
  const formattedDate = parts.length === 3 ? [parts[2], parts[1], parts[0]].join("-") : String(date || "");
  return sanitizeSlug(`${formattedDate}-${title || ""}`) || sanitizeSlug(fallbackSlug);
}

function toPosixPath(value) {
  return String(value || "").split(path.sep).join("/");
}

async function findPosterPath(eventDir, folderName) {
  const files = await fs.readdir(eventDir, { withFileTypes: true });
  const poster = files.find((entry) => entry.isFile() && posterNames.has(entry.name.toLowerCase()));
  if (poster) return toPosixPath(path.join("content", "events", folderName, poster.name));

  return findFirstGalleryImagePath(eventDir, folderName);
}

async function findFirstGalleryImagePath(eventDir, folderName) {
  const galleryDir = path.join(eventDir, "gallery");

  try {
    const files = await fs.readdir(galleryDir, { withFileTypes: true });
    const image = files
      .filter((entry) => entry.isFile() && galleryImageExtensions.has(path.extname(entry.name).toLowerCase()))
      .sort((left, right) => left.name.localeCompare(right.name, "en", { numeric: true }))[0];

    return image ? toPosixPath(path.join("content", "events", folderName, "gallery", image.name)) : "";
  } catch {
    return "";
  }
}

function buildFeedContent(feed) {
  return [
    "/* AUTO-GENERATED by tools/generate-event-pages.mjs */",
    "",
    `window.TACT_EVENT_FEED = ${JSON.stringify(feed, null, 2)};`,
    ""
  ].join("\n");
}

async function collectFeed() {
  const entries = await fs.readdir(eventsRoot, { withFileTypes: true });
  const feed = [];

  for (const entry of entries) {
    if (!entry.isDirectory() || entry.name.startsWith("_")) continue;

    const eventDir = path.join(eventsRoot, entry.name);
    const eventJsonPath = path.join(eventDir, "event.json");

    try {
      const raw = await fs.readFile(eventJsonPath, "utf8");
      const parsed = JSON.parse(raw.replace(/^\uFEFF/, ""));
      const slug = String(parsed.slug || entry.name).trim();
      const title = String(parsed.title || "").trim();
      const date = String(parsed.date || "").trim();
      const fileSlug = buildPageSlug(date, title, slug);
      if (!slug || !title || !date || !fileSlug) continue;

      feed.push({
        slug,
        title,
        date,
        time: String(parsed.time || "").trim(),
        location: String(parsed.location || "").trim(),
        status: String(parsed.status || "").trim(),
        folder: toPosixPath(path.join("content", "events", entry.name)),
        pageUrl: toPosixPath(path.join("events", `${fileSlug}.html`)),
        poster: await findPosterPath(eventDir, entry.name),
        teaser: String(parsed.teaser || "").trim(),
        homepageMatter: String(parsed.homepageMatter || "").trim()
      });
    } catch {
      // Skip folders without valid event metadata.
    }
  }

  feed.sort((left, right) => String(left.date || "").localeCompare(String(right.date || "")));
  return feed;
}

