import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const siteRoot = path.resolve(scriptDir, "..");
const logosDir = path.join(siteRoot, "logobelt");
const outputPath = path.join(logosDir, "logobelt-manifest.js");
const allowedExtensions = new Set([".png", ".jpg", ".jpeg", ".webp", ".svg", ".avif"]);

function toPosixPath(value) {
  return String(value || "").split(path.sep).join("/");
}

function cleanLabel(fileName) {
  return String(fileName || "")
    .replace(/\.[^.]+$/, "")
    .replace(/^logo[\s-_]*/i, "")
    .replace(/[\s_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function sortByLabel(left, right) {
  return cleanLabel(left.name).localeCompare(cleanLabel(right.name), "en", { sensitivity: "base" });
}

async function main() {
  await fs.mkdir(logosDir, { recursive: true });
  const entries = await fs.readdir(logosDir, { withFileTypes: true });

  const logos = entries
    .filter(function (entry) {
      return entry.isFile() && allowedExtensions.has(path.extname(entry.name).toLowerCase());
    })
    .sort(sortByLabel)
    .map(function (entry) {
      return {
        name: cleanLabel(entry.name),
        src: toPosixPath(path.join("logobelt", entry.name))
      };
    });

  const content = [
    "/* AUTO-GENERATED by tools/generate-logo-belt-manifest.mjs */",
    "",
    "window.TACT_LOGO_BELT = " + JSON.stringify(logos, null, 2) + ";",
    ""
  ].join("\n");

  await fs.writeFile(outputPath, content, "utf8");
}

main().catch(function (error) {
  console.error(error);
  process.exitCode = 1;
});
